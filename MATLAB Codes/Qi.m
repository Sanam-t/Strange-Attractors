function Qi
%==========================================================================
% Matlab program to plot a phase portrait of the 3D chaotic system
%==========================================================================
clear
clc
clf
%==================== Parameters ==========================================
a =30; b=10; c=1; d=10;
%===================== Initial conditions =================================
initial=[-0.5, -1, 7, 0.1]; 
% =============== Solves ODEs =============================================
deq1=@(t,x) [a*(x(2)-x(1))+x(2)*x(3)*x(4); b*(x(1)+x(2))-x(1)*x(3)*x(4);...
    -c*x(3)+x(1)*x(2)*x(4); -d*x(4)+x(1)*x(2)*x(3)];
[~,sol] = ode45(deq1,[0, 200],initial);
%============= Runs simulation ============================================
    plot3(sol(:,1),sol(:,2),sol(:,3)),grid
    title('Qi Attractor','fontsize',8)
    xlabel('x','fontsize',8)
    ylabel('y','fontsize',8)
    zlabel('z','fontsize',8)
%================= End of program =========================================