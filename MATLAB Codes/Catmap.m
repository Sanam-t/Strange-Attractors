function Catmap
%==========================================================================
% Matlab program to plot a phase portrait of the 3D chaotic system
%==========================================================================
clear
clc
clf
%==================== Parameters ==========================================
d=0.15;
%===================== Initial conditions =================================
x(1)=1; 
y(1)=1;
% =============== Solves ODEs =============================================
for k=2:5
    x(k)=1-x(k-1)+y(k-1)+d*cos(2*pi*y(k-1));
    y(k)=x(k-1)+2*y(k-1);
end
%============= Runs simulation ============================================
    plot(x,y,'.'),grid
    title('Cat Map','fontsize',8)
    xlabel('x','fontsize',8)
    ylabel('y','fontsize',8)
%================= End of program =========================================