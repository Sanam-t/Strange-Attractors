function Henonmap
%==========================================================================
% Matlab program to plot a phase portrait of the 3D chaotic system
%==========================================================================
clear
clc
clf
%==================== Parameters ==========================================
a = 1.4; b=0.3;
%===================== Initial conditions =================================
x(1)=0; 
y(1)=0;
% =============== Solves ODEs =============================================
for k=2:10000
    x(k)=1-a*(x(k-1)^2)+y(k-1);
    y(k)=b*x(k-1);
end
%============= Runs simulation ============================================
    plot(x,y,'.'),grid
    title('Henon Map','fontsize',8)
    xlabel('x','fontsize',8)
    ylabel('y','fontsize',8)
%================= End of program =========================================